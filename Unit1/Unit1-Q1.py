
l1 = [10, 20, 30, 40, 50]
print("Original List:", l1)
print("First element:", l1[0])
print("Last element:", l1[-1])
l1.append(60)      
print("After adding 60:", l1)
l1.remove(30)     
print("After removing 30:", l1)
l1.sort()
print("Sorted List:", l1)
l1.reverse()
print("Reversed List:", l1)
